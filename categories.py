def get_category_emoji(category_name: str) -> str:
    """
    Возвращает эмодзи для категории по её названию (без эмодзи)
    """
    emoji_map = {
        # Доходы
        "Зарплата": "💼",
        "Фриланс": "💻",
        "Инвестиции": "📈",
        "Подарки": "🎁",
        "Другое": "💰",
        
        # Расходы
        "Продукты": "🛒",
        "Транспортные": "🚗",
        "Культура": "🎭",
        "Бытовые": "🏠",
        "Одежда": "👕",
        "Красота": "💄",
        "Здоровье": "💊",
        "Праздники": "🎉",
        "Ремонт": "🔧",
        "Рестораны": "🍽️",
        "Ипотека": "🏠",
        "Аренда": "🏠",
        "Долг": "💳",
        "Посиделки": "🍻",
        "Связь": "📱"
    }
    
    # Если категория уже содержит эмодзи, возвращаем как есть
    if any(emoji in category_name for emoji in ["💰", "💼", "💻", "📈", "🎁", "🛒", "🚗", "🎭", "🏠", "👕", "💄", "💊", "🎉", "🔧", "🍽️", "💳", "🍻", "📱", "📦"]):
        return ""
    
    return emoji_map.get(category_name, "📌")

def clean_category_name(category: str) -> str:
    """
    Очищает название категории от эмодзи
    """
    import re
    # Удаляем эмодзи из начала строки
    cleaned = re.sub(r'^[^\w\s]+\s*', '', category)
    return cleaned.strip()